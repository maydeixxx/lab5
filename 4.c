#include <stdio.h>

int main() {
    int n;
    
    // Ввод значения n
    printf("Введите значение n (n >= 2): ");
    scanf("%d", &n);

    // Проверка на корректность ввода
    if (n < 2) {
        printf("Пожалуйста, введите значение n больше или равное 2.\n");
        return 1;
    }

    int count = 0; // Счетчик простых чисел
    int num = 2;   // Начальное число для проверки на простоту

    // Цикл для нахождения простых чисел
    while (num <= n) {
        int is_prime = 1; // Предполагаем, что число простое
        int divisor = 2;  // Делитель для проверки

        // Проверка на простоту числа num
        do {
            if (num % divisor == 0 && divisor < num) {
                is_prime = 0; // Если есть делитель, то число не простое
            }
            divisor++;
        } while (divisor * divisor <= num); // Проверяем до квадратного корня из num

        // Если число простое, выводим его и увеличиваем счетчик
        if (is_prime) {
            printf("%d ", num);
            count++;
        }

        num++; // Переход к следующему числу
    }

    // Вывод количества простых чисел
    printf("\nКоличество простых чисел в диапазоне от 2 до %d: %d\n", n, count);

    return 0;
}
